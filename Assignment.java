public class Assignment
{
public static void main(String[]args)
{
int x=5;
System.out.println(x=5);
System.out.println(x+=2);
System.out.println(x-=2);
System.out.println(x*=2);
System.out.println(x/=5);
System.out.println(x%=5);
System.out.println(x&=5);
System.out.println(x|=5);
System.out.println(x^=2);
System.out.println(x>>=2);
System.out.println(x<<=6);
}
}